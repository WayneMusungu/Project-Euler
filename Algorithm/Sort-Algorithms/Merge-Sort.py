# Merge Sort

# This is a divide and conquer algorithm
# Divide the input array into two halves and keep halving recursively until they become too small that cannot be broken further
# Merge halves by sorting them

# Time complexity O(NLogN) - Linearithmic (log linear)
# This is slightly slower than a linear algorithm, but it's much better than quadratic algorithm
# Space Complexity O(N) => INside the merge function we create subarrays, which takes space memory

# Merge sort algorithm is better than other sorting algorithms:
 # It performs faster than selection and insertion sort but it requires O(n) sopace complexity


# When to use Merge Sort
 # When you need stable sort
 # When average exppected time is O(NLogN)

# When to avoid Merge Sort
 # When space is a concern
